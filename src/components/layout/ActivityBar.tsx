export function ActivityBar() {
  const { sidebarOpen, sidebarPanel, toggleSidebar, setSidebarPanel, toggleSettings } = useUIStore();

  const handlePanelClick = (panelId: PanelId) => {
    if (!sidebarOpen) {
      setSidebarPanel(panelId);
      toggleSidebar();
    } else if (sidebarPanel === panelId) {
      toggleSidebar();
    } else {
      setSidebarPanel(panelId);
    }
  };

  return (
    <div className="activity-bar">
      <div className="activity-bar-top">
        {PANEL_ICONS.map((item) => (
          <button
            key={item.id}
            className={`activity-bar-btn ${sidebarOpen && sidebarPanel === item.id ? "activity-bar-btn-active" : ""}`}
            onClick={() => handlePanelClick(item.id)}
            title={item.label}
          >
            {item.icon}
          </button>
        ))}
      </div>
      <div className="activity-bar-bottom">
        <button
          className="activity-bar-btn"
          onClick={() => toggleSettings()}
          title="Settings"
        >
          {SettingsIcon}
        </button>
      </div>
    </div>
  );
}
